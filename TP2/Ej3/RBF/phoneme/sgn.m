function v = sgn(z)
    % ------------------------------------------------------
    % Funcion signo
    % ------------------------------------------------------
    % ENTRADA
    % z = valor real del dominio
    % ------------------------------------------------------
    % SALIDA
    % v = valor -1 o 1 del rango
    % ------------------------------------------------------
    
    if z<0
        v=-1;
    else
        v=1;
    end
end